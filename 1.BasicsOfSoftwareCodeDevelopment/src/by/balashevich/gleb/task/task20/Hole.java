package by.balashevich.gleb.task.task20;

public class Hole {
    private int aSide;
    private int bSide;

    public Hole() {
    }

    public Hole(int aSide, int bSide) {
        this.aSide = aSide;
        this.bSide = bSide;
    }

    public int getaSide() {
        return aSide;
    }

    public void setaSide(int aSide) {
        this.aSide = aSide;
    }

    public int getbSide() {
        return bSide;
    }

    public void setbSide(int bSide) {
        this.bSide = bSide;
    }
}
